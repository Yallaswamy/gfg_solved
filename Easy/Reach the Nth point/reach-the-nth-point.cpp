class Solution{
	public:
		int nthPoint(int n){
		    // Code here
		    int a=1,b=2;
		    for(int i=2;i<=n;i++){
		        int temp=(a+b)%1000000007;
		        a=b;
		        b=temp;
		    }
		    return a;
		    
		}
};

//{ Driver Code Starts.
int main(){
	int tc;
	cin >> tc;
	while(tc--){
		int n;
		cin >> n;
		Solution ob;
		int ans  = ob.nthPoint(n);
		cout << ans <<"\n";
	}
	return 0;
}
