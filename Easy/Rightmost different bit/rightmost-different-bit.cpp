class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
      
        int c=0;
        while(n || m){
            int k=n&1;
            int k1=m&1;
            if(k!=k1){
                return c+1;
            }
            c+=1;
            n=n>>1;
            m=m>>1;
        }
        return -1;
    }
};

