class Solution {
  public:
    int minSteps(int d) {
        // code here
        int sum=0,s=0;
        while(sum<d){
            sum+=s;
            s++;
        }
        while((sum-d)%2){
            sum+=s;
            s+=1;
        }
        return s-1;
    }
};

